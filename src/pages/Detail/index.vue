<script>
import ImageList from './ImageList/ImageList'
import Zoom from './Zoom/Zoom'

import { mapGetters } from 'vuex'

export default {
  name: 'Detail',
  data() {
    return {
      // 加入购物车的商品数量
      skuNum: 1
    }
  },
  components: {
    ImageList,
    Zoom
  },
  mounted() {
    this.$store.dispatch('getDetailInfo', this.$route.params.skuId)
    // console.log(this.$route.params.skuid);
  },
  computed: {
    ...mapGetters(['categoryView', 'skuInfo', 'spuSaleAttrList']),
    // 对传递给Zoom的子组件的数据兜底操作
    skuImageList() {
      return this.skuInfo.skuImageList || []
    }
  },
  methods: {
    // 商品售卖属性切换高亮
    changeActive(slAttr, arr) {
      // 排它思想 商品售卖属性切换高亮
      // 先将所有的 idCheck都置为 0
      arr.forEach(item => {
        item.isChecked = '0'
      })
      // 再将点击的那个置为 1
      slAttr.isChecked = '1'
    },
    // 加入购物车的数量动态变化
    skuNumChange(e) {
      // 将用户输入的数字字符串 转变为数字
      let val = e.target.value * 1
      if(isNaN(val) || val < 1) {
        this.skuNum = 1
      } else {
        // 大于1的不能出现小数
        this.skuNum = parseInt(val)
      }
    },
    // 点击加入购物车按钮
    async addShopCart() {
      // 1.发送数据，将商品 id和 加入购物车的商品数量传入，让服务器保存下来
      // 2.数据发送成功 路由跳转
      // 3.发送失败 提示用户
      try {
        // this.$store.dispatch('addOrUpdateShopCart') 本质上就是调用 addOrUpdateShopCart函数，这个函数的返回值是一个Promise
        // 使用await将其解析，对其进行异常捕获，
        await this.$store.dispatch('addOrUpdateShopCart', {skuId: this.$route.params.skuId, skuNum: this.skuNum})
        // 在进行路由跳转的时候还需要将一些产品信息带给下一级路由
        // 一些简单的数据skuNum，通过路由query参数给下一级路由传递过去
        // 利用 本地存储 || 会话存储 进行复杂数据的传递  本地存储 || 会话存储存储的一般是字符串
        // 路由跳转
        this.$router.push({ name: 'AddCartSuccess', query: { skuNum: this.skuNum } })
        // 将商品信息存储到浏览器的会话存储中
        sessionStorage.setItem('SKUINFO', JSON.stringify(this.skuInfo))
      } catch (error) { 
        // 捕获到异常 将其在对话框中弹出
        alert(error.message)
      }
    }
  },
}
</script>

